package day4practice;

public class Postpreincrement {
    public static void main(String[] args) {

        int sayi= 10;
        sayi++;
        System.out.println(sayi);// 11
        ++sayi;
        System.out.println(sayi);//12


        System.out.println(sayi++);//12
        System.out.println(sayi);//13

        System.out.println(++sayi);//14
        System.out.println(sayi);//14










    }
}
