package day4practice;

public class prepostincrement {
    public static void main(String[] args) {


        int sayi=10;

        System.out.println(sayi++);//10
        System.out.println(sayi);//11

        System.out.println(++sayi);//12
        System.out.println(sayi);//12

        System.out.println(sayi++);//12
        System.out.println(sayi);//13
    }
}
